/*===========================================================================*
 * Function: Utils_Itoa
 *===========================================================================*/
char* Utils_Itoa(unsigned int number, unsigned int base)
{
    char symbols[]= "0123456789ABCDEF";
    char buffer[UTILS_32BIT_NUMBER_STRING_MAX_LEN];
    char *ptr;

    ptr = &buffer[(UTILS_32BIT_NUMBER_STRING_MAX_LEN - 1u)];
    *ptr = '\0';

    do
    {
        *--ptr = symbols[number % base];
        number /= base;
    } while (number != 0);

    return (ptr);
}

